#include "libft.h"

int		ft_intlen(int n)
{
	int		len;

	len = ((n >= 0) ? 1 : 2);
	n = ((n < 0) ? n : -n);
	while (n <= -10 && len++)
		n = (n / -10) * -1;
	return (len);
}
