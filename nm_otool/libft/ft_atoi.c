#include <libft.h>

int	ft_atoi(const char *s)
{
	int sign;
	int out;

	out = 0;
	sign = 1;
	while (*s == ' ' || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-' || *s == '+')
		sign = (*s++ == '+' ? 1 : -1);
	while (ft_isdigit(*s))
		out = out * 10 - '0' + *s++;
	return (out * sign);
}
