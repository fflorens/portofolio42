#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	max;

	i = ft_strlen(dst);
	max = i + ft_strlen(src);
	if (size < i)
		return (size + ft_strlen(src));
	j = 0;
	while ((i + j) < size - 1 && src[j] != '\0')
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	return (max);
}
